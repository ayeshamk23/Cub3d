#include "../libft.h"

int	ft_setinset(char *s1, char *s2)
{
	int	i;

	if (!s1 || !s2)
		return (0);
	i = 0;
	while (s1[i])
	{
		if (ft_inset(s2, s1[i]))
		{
			return (i);
		}
		i++;
	}
	return (-1);
}
